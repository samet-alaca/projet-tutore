package metier;

public class variation {
	
	private int lesserValue;
	private int upperValue;
	
	public variation(int lesserValue, int upperValue) {
		this.lesserValue = lesserValue;
		this.upperValue = upperValue;
	}

	public int getLesserValue() {
		return lesserValue;
	}

	public void setLesserValue(int lesserValue) {
		this.lesserValue = lesserValue;
	}

	public int getUpperValue() {
		return upperValue;
	}

	public void setUpperValue(int upperValue) {
		this.upperValue = upperValue;
	}
	
	
	
	

}
